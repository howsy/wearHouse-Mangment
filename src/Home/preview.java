    public void LoadDataInTable(List<HomeBean> list){
    
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        for( HomeBean bean : list){
        Vector v = new Vector();
        v.add(bean.getId());
        v.add(bean.getPname());
        v.add(bean.getPrice());
        v.add(bean.getQty());
        
        dtm.addRow(v);
        }
        
    }
    public void loadTable(){
        
        HomeBal bal = new HomeBal();
        List list = bal.loadData();
        LoadDataInTable(list);
    
    }

     

