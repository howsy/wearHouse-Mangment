    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        
        if (row <0){
            
            JOptionPane.showMessageDialog(null, "Please Select Row to Edit OR Delete");
        
        }else{
             id = (int) jTable1.getValueAt(row,0);
            
            HomeBal bal = new HomeBal();
            
            HomeBean bean = bal.ReturnAllDataToTable(id);
            nametxt.setText(bean.getPname());
            pricetxt.setText(String.valueOf(bean.getPrice()));
            qtytxt.setText(String.valueOf(bean.getQty()) );
        }
        
        
    }//GEN-LAST:event_jTable1MouseClicked
